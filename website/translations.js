// ===== Shared i18n Functions =====

function setLanguage(lang) {
  var t = TRANSLATIONS[lang];
  if (!t) return;

  document.documentElement.lang = lang;
  localStorage.setItem('fewo-lang', lang);

  // Update toggle buttons
  var otherFlag = lang === 'de' ? '\uD83C\uDDEC\uD83C\uDDE7' : '\uD83C\uDDE9\uD83C\uDDEA';
  var otherTitle = lang === 'de' ? 'English' : 'Deutsch';
  var langToggle = document.getElementById('langToggle');
  var langToggleMobile = document.getElementById('langToggleMobile');
  if (langToggle) { langToggle.textContent = otherFlag; langToggle.title = otherTitle; }
  if (langToggleMobile) { langToggleMobile.textContent = otherFlag; langToggleMobile.title = otherTitle; }

  // Text content
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (t[key]) el.textContent = t[key];
  });

  // HTML content
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-html');
    if (t[key]) el.innerHTML = t[key];
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-placeholder');
    if (t[key]) el.placeholder = t[key];
  });

  // Alt texts
  document.querySelectorAll('[data-i18n-alt]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-alt');
    if (t[key]) el.alt = t[key];
  });

  // Title attributes
  document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-title');
    if (t[key]) el.title = t[key];
  });

  // Aria labels
  document.querySelectorAll('[data-i18n-aria]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-aria');
    if (t[key]) el.setAttribute('aria-label', t[key]);
  });

  // Counter suffixes
  document.querySelectorAll('[data-i18n-suffix]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-suffix');
    if (t[key]) {
      el.dataset.suffix = t[key];
      var count = el.dataset.count;
      if (count) el.textContent = count + t[key];
    }
  });

  // Meta tags
  document.title = t['meta.title'] || document.title;
  var metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && t['meta.description']) metaDesc.content = t['meta.description'];
  var ogTitle = document.querySelector('meta[property="og:title"]');
  if (ogTitle && t['meta.ogTitle']) ogTitle.content = t['meta.ogTitle'];
  var ogDesc = document.querySelector('meta[property="og:description"]');
  if (ogDesc && t['meta.ogDescription']) ogDesc.content = t['meta.ogDescription'];
}

function initLanguage() {
  var savedLang = localStorage.getItem('fewo-lang') || 'de';
  if (savedLang !== 'de') {
    setLanguage(savedLang);
  }

  var langToggle = document.getElementById('langToggle');
  if (langToggle) {
    langToggle.addEventListener('click', function() {
      var current = document.documentElement.lang || 'de';
      setLanguage(current === 'de' ? 'en' : 'de');
    });
  }

  var langToggleMobile = document.getElementById('langToggleMobile');
  if (langToggleMobile) {
    langToggleMobile.addEventListener('click', function() {
      var current = document.documentElement.lang || 'de';
      setLanguage(current === 'de' ? 'en' : 'de');
      var mobileMenu = document.getElementById('mobileMenu');
      if (mobileMenu) mobileMenu.classList.remove('open');
    });
  }
}
